print(str(num1) + " + " + str(num2) + " = " + str(result))

result = float(num1) * float(num2)
print(str(num1) + " * " + str(num2) + " = " + str(result))

result = float(num1) / float(num2)
print(str(num1) + " / " + str(num2) + " = " + str(result))

result = float(num1) - float(num2)
print(str(num1) + " - " + str(num2) + " = " + str(result))

result = float(num1) % float(num2)
print(str(num1) + " % " + str(num2) + " = " + str(result))

result = float(num1) ** float(num2)
print(str(num1) + " ** " + str(num2) + " = " + str(result))

